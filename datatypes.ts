// Primitive datatypes
// 1. string
let userName: string = "Nure Aalam";
console.log(userName);
// 2. number
let age: number = 24;
console.log(age);
// 3. boolean
let isMarried: boolean = false;
console.log(isMarried);
// 4. null
let data: null = null;
console.log(data);
// 5. undefined
let value: undefined = undefined;
console.log(value);

// Object datatypes
// 1. Array
let names: string[] = ["Sohag", "Saymon"];
console.log(names);
let ages: number[] = [24, 23];
console.log(ages);
let isMarrieds: boolean[] = [false, false];
console.log(isMarrieds);
// 2. Object
let user: { name: string, age: number} = { name: "Nure Alam", age: 24};
console.log(user);
// 3. Tuple
let person: [string, number] = ["Nure Alam", 24];
console.log(person);

