"use strict";
// Primitive datatypes
// 1. string
let userName = "Nure Aalam";
console.log(userName);
// 2. number
let age = 24;
console.log(age);
// 3. boolean
let isMarried = false;
console.log(isMarried);
// 4. null
let data = null;
console.log(data);
// 5. undefined
let value = undefined;
console.log(value);
// Object datatypes
// 1. Array
let names = ["Sohag", "Saymon"];
console.log(names);
let ages = [24, 23];
console.log(ages);
let isMarrieds = [false, false];
console.log(isMarrieds);
// 2. Object
let user = { name: "Nure Alam", age: 24 };
console.log(user);
// 3. Tuple
let person = ["Nure Alam", 24];
console.log(person);
